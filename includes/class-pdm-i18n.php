    public function init(): void
    {
        add_filter('gettext', [$this, 'filter_gettext'], 20, 3);
    }

    public function filter_gettext(string $translation, string $text, string $domain): string
    {
        if ('private-document-manager' !== $domain) {
            return $translation;
        }

        if ('it' === self::get_language()) {
            return self::ITALIAN_MAP[$text] ?? $translation;
        }

        return $translation;
    }

    public static function get_language(): string
    {
        $language = get_option(self::OPTION_LANGUAGE, 'en');

        return in_array($language, ['en', 'it'], true) ? $language : 'en';
    }

    public static function sanitize_language($value): string
    {
        return in_array($value, ['en', 'it'], true) ? $value : 'en';
    }
